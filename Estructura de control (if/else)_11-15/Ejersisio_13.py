#Mayor o iguales
num1=float(input("Ingrese primer número: "))
num2=float(input("Ingrese segundo número: "))
if num1>num2:
    print("El primer número ingresado es mayor")
if num2>num1:
    print("El segundo número ingresado es mayor")
if num1==num2:
    print("Ambos números ingresados son iguales")